create or replace function public.fn_reporte_carga_event_log()
returns trigger
language plpgsql
as $$
declare
  event_name text;
  ref_text text;
  ref_date date;
  row_new public.reporte_carga%rowtype;
  row_old public.reporte_carga%rowtype;
begin
  if tg_op = 'INSERT' then
    event_name := 'DT_CREATED';
    row_new := new;
    ref_text := coalesce(new.data_ref, '');
  elsif tg_op = 'UPDATE' then
    if to_jsonb(old) = to_jsonb(new) then
      return new;
    end if;
    event_name := case
      when coalesce(old.status,'') is distinct from coalesce(new.status,'') then 'STATUS_CHANGED'
      when coalesce(old.grade_carregamento,'') is distinct from coalesce(new.grade_carregamento,'')
        or coalesce(old.fim_carregamento,'') is distinct from coalesce(new.fim_carregamento,'') then 'GRADE_CHANGED'
      when coalesce(old.peso_liquido,'') is distinct from coalesce(new.peso_liquido,'')
        or coalesce(old.toneladas,'') is distinct from coalesce(new.toneladas,'') then 'WEIGHT_CHANGED'
      else 'DT_UPDATED'
    end;
    row_new := new;
    row_old := old;
    ref_text := coalesce(new.data_ref, old.data_ref, '');
  elsif tg_op = 'DELETE' then
    event_name := 'DT_REMOVED_FROM_CURRENT';
    row_old := old;
    ref_text := coalesce(old.data_ref, '');
  end if;

  begin
    ref_date := to_date(ref_text, 'DD/MM/YYYY');
  exception when others then
    ref_date := null;
  end;

  insert into public.dt_logs (
    dt, data_ref, event_type,
    status_before, status_after,
    toneladas, tipo_carga, tipo_operacao, turno,
    transportadora, grade_carregamento, fim_carregamento,
    hora_chegada, n_portaria, data_operacao, source, payload
  )
  values (
    coalesce(row_new.dt, row_old.dt),
    ref_text,
    event_name,
    row_old.status,
    row_new.status,
    public.fn_parse_peso_kg(coalesce(row_new.peso_liquido, row_new.toneladas, row_old.peso_liquido, row_old.toneladas)),
    coalesce(row_new.paletizacao, row_old.paletizacao),
    coalesce(row_new.tipo_operacao, row_old.tipo_operacao),
    case
      when coalesce(row_new.fim_carregamento, row_old.fim_carregamento, '') ~ '^\d{2}/\d{2}/\d{4}\s+\d{2}:\d{2}' then
        case
          when extract(hour from to_timestamp(coalesce(row_new.fim_carregamento, row_old.fim_carregamento), 'DD/MM/YYYY HH24:MI')) between 7 and 14 then 'T1'
          when extract(hour from to_timestamp(coalesce(row_new.fim_carregamento, row_old.fim_carregamento), 'DD/MM/YYYY HH24:MI')) between 15 and 22 then 'T2'
          else 'T3'
        end
      else null
    end,
    coalesce(row_new.transportadora, row_old.transportadora),
    coalesce(row_new.grade_carregamento, row_old.grade_carregamento),
    coalesce(row_new.fim_carregamento, row_old.fim_carregamento),
    coalesce(row_new.hora_chegada, row_old.hora_chegada),
    coalesce(row_new.n_portaria, row_old.n_portaria),
    ref_date,
    coalesce(row_new.source, row_old.source, 'DASHBOARD'),
    jsonb_build_object('old', to_jsonb(row_old), 'new', to_jsonb(row_new))
  );

  if tg_op = 'DELETE' then
    return old;
  end if;
  return new;
end;
$$;

drop trigger if exists trg_reporte_carga_event_log on public.reporte_carga;
create trigger trg_reporte_carga_event_log
