create or replace function public.fn_create_grade_snapshot(
  p_data_ref text,
  p_source text default 'DASHBOARD',
  p_evento text default null,
  p_planejado_suzano_kg numeric default null
)
returns bigint
language plpgsql
as $$
declare
  new_id bigint;
  ref_date date;
begin
  begin
    ref_date := to_date(p_data_ref, 'DD/MM/YYYY');
  exception when others then
    ref_date := null;
  end;

  insert into public.grade_snapshots (
    data_ref, data_operacao, source, evento, planejado_suzano_kg,
    nossa_grade_kg, realizado_kg, pendente_kg,
    dts_total, dts_abertas, dts_expedidas, dts_no_show, dts_recusadas,
    detalhes
  )
  select
    p_data_ref,
    ref_date,
    coalesce(p_source, 'DASHBOARD'),
    p_evento,
    p_planejado_suzano_kg,
    coalesce(sum(public.fn_parse_peso_kg(coalesce(peso_liquido, toneladas, '0')))
      filter (where coalesce(status,'') not in ('NO SHOW','VEICULO RECUSADO')), 0),
    coalesce(sum(public.fn_parse_peso_kg(coalesce(peso_liquido, toneladas, '0')))
      filter (where coalesce(status,'') in ('EXPEDIDO','EM FATURAMENTO')), 0),
    greatest(
      coalesce(sum(public.fn_parse_peso_kg(coalesce(peso_liquido, toneladas, '0')))
        filter (where coalesce(status,'') not in ('NO SHOW','VEICULO RECUSADO')), 0)
      -
      coalesce(sum(public.fn_parse_peso_kg(coalesce(peso_liquido, toneladas, '0')))
        filter (where coalesce(status,'') in ('EXPEDIDO','EM FATURAMENTO')), 0),
      0
    ),
    count(*)::integer,
    count(*) filter (where coalesce(status,'') not in ('EXPEDIDO','NO SHOW','VEICULO RECUSADO'))::integer,
    count(*) filter (where coalesce(status,'') = 'EXPEDIDO')::integer,
    count(*) filter (where coalesce(status,'') = 'NO SHOW')::integer,
    count(*) filter (where coalesce(status,'') = 'VEICULO RECUSADO')::integer,
    jsonb_build_object(
      'por_status', coalesce((
        select jsonb_object_agg(status_key, qtd)
        from (
          select coalesce(status,'SEM STATUS') as status_key, count(*) as qtd
          from public.reporte_carga
          where data_ref = p_data_ref
          group by coalesce(status,'SEM STATUS')
        ) s
      ), '{}'::jsonb),
      'por_operacao', coalesce((
        select jsonb_object_agg(op_key, kg)
        from (
          select coalesce(tipo_operacao,'SEM OPERACAO') as op_key,
                 sum(public.fn_parse_peso_kg(coalesce(peso_liquido, toneladas, '0'))) as kg
          from public.reporte_carga
          where data_ref = p_data_ref
            and coalesce(status,'') not in ('NO SHOW','VEICULO RECUSADO')
          group by coalesce(tipo_operacao,'SEM OPERACAO')
        ) o
      ), '{}'::jsonb)
    )
  from public.reporte_carga
  where data_ref = p_data_ref
  returning id into new_id;

  return new_id;
end;
$$;

