with ranked as (
  select id,
         row_number() over (
           partition by dt, data_ref
           order by updated_at desc nulls last, created_at desc nulls last, id desc
         ) as rn
  from public.reporte_carga
)
delete from public.reporte_carga rc
using ranked r
where rc.id = r.id
  and r.rn > 1;

