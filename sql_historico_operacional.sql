-- ---------------------------------------------------------------------
-- 2) Weight parser used by snapshots/reportes.
--    Handles examples like: 9202, 9.202, 9202,50, 1.493,51.
-- ---------------------------------------------------------------------
create or replace function public.fn_parse_peso_kg(raw text)
returns numeric
language plpgsql
immutable
as $$
declare
  s text;
  n numeric;
begin
  s := trim(coalesce(raw, ''));
  if s = '' then
    return 0;
  end if;

  s := regexp_replace(s, '\s+', '', 'g');

  if position(',' in s) > 0 and position('.' in s) > 0 then
    if strpos(reverse(s), ',') < strpos(reverse(s), '.') then
      s := replace(replace(s, '.', ''), ',', '.');
    else
      s := replace(s, ',', '');
    end if;
  elsif position(',' in s) > 0 then
    s := replace(replace(s, '.', ''), ',', '.');
  elsif s ~ '^\d{1,3}(\.\d{3})+$' then
    s := replace(s, '.', '');
  end if;

  s := regexp_replace(s, '[^0-9\.-]', '', 'g');
  if s = '' or s = '-' or s = '.' then
    return 0;
  end if;

  n := s::numeric;
  return floor(n);
exception when others then
  return 0;
end;
$$;

